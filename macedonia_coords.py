def get_coords(city_name):
    """Get coordinates for a city/municipality name."""
    if not city_name:
        return None
    
    name = str(city_name).strip()
    
    # Direct match
    if name in MACEDONIA_COORDS:
        return MACEDONIA_COORDS[name]
    
    # Case-insensitive match
    for city, coords in MACEDONIA_COORDS.items():
        if city.lower() == name.lower():
            return coords
    
    # Partial match (city name in key)
    name_lower = name.lower()
    for city, coords in MACEDONIA_COORDS.items():
        if name_lower in city.lower() or city.lower() in name_lower:
            return coords
    
    return None


def get_all_cities():
    """Return list of all cities/municipalities."""
    return sorted(MACEDONIA_COORDS.keys())


def get_city_count():
    """Return count of cities."""
    return len(MACEDONIA_COORDS)


# Fallback population for cities not in our data
DEFAULT_POPULATION = 20000  # Used for cities we don't have data for

